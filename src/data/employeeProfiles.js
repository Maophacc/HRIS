export const getEmployeeProfile = (employee) => {
  if (!employee) return null;
  const extra = baseProfiles[employee.id] || defaultProfile;
  return {
    ...employee,
    ...extra,
    code: employee.empCode,
    dob: formatDate(employee.birthDate),
    startDate: formatDate(employee.startDate),
    contractEndDate: formatDate(employee.contractEndDate),
    salaryFormatted: formatSalary(employee.salary),
    insuranceSalaryFormatted: formatSalary(extra.insuranceSalary || employee.salary),
  };
};

